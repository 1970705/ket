        return lookWords
    }

    fun getWordsForLevel(levelIndex: Int): List<Word> {
        // Each level has 6 words
        val startIndex = levelIndex * 6
        val endIndex = minOf(startIndex + 6, lookWords.size)
        return if (startIndex < lookWords.size) {
            lookWords.subList(startIndex, endIndex)
        } else {
            emptyList()
        }
    }

    fun getWordById(wordId: String): Word? {
        return lookWords.find { it.id == wordId }
    }
